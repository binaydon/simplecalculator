
namespace ConsoleApp3;

public class calculator
{
    public double a, b, c;

    public double add()
    {
        c = a + b;
        return c;
    }
    public double sub()
    {
        c = a - b;
        return c;
    }
    public double mul()
    {
        c = a * b;
        return c;
    }
    public double div()
    {
        c = a * b;
        return c;
    }

}

